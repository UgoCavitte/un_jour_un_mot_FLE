import 'package:shared_preferences/shared_preferences.dart';
import 'package:un_jour_un_mot/data/data.dart';

abstract class GestionMemoire {
  static final SharedPreferencesAsync _memoire = SharedPreferencesAsync();

  ///////////////////////////////////////////////////////////////
  //////////////////////////// KEYS /////////////////////////////
  ///////////////////////////////////////////////////////////////

  static const String _keyTutoriel = "tutoriel";

  ///////////////////////////////////////////////////////////////
  //////////////////////// ENREGISTREURS ////////////////////////
  ///////////////////////////////////////////////////////////////

  static void enregistrerTutoriel(bool statut) {
    _memoire.setBool(_keyTutoriel, statut);
  }

  ///////////////////////////////////////////////////////////////
  ////////////////////////// LECTEURS ///////////////////////////
  ///////////////////////////////////////////////////////////////

  static Future<void> chargerTutoriel() async {
    Data.tutorielDebut = await _memoire.getBool(_keyTutoriel) ?? true;
    return;
  }
}
